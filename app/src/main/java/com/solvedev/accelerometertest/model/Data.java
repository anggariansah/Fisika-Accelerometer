package com.solvedev.accelerometertest.model;

public class Data {

    private int id;
    private String title;
    private String time;
    private String percepatan;
    private String kecepatan;
    private String posisi;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getPercepatan() {
        return percepatan;
    }

    public void setPercepatan(String percepatan) {
        this.percepatan = percepatan;
    }

    public String getKecepatan() {
        return kecepatan;
    }

    public void setKecepatan(String kecepatan) {
        this.kecepatan = kecepatan;
    }

    public String getPosisi() {
        return posisi;
    }

    public void setPosisi(String posisi) {
        this.posisi = posisi;
    }
}
